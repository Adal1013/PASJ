import java.util.ArrayList;

public class DiccionarioMes {
    private int numMes;
    private int numDia;

    public DiccionarioMes(int numMes, int numDia) {
        this.numMes = numMes;
        this.numDia = numDia;
    }

    public int getNumMes() {
        return numMes;
    }

    public int getNumDia() {
        return numDia;
    }
}
